export const Math = {
    sum : (a: number, b: number) => {
        return a + b;
    },

    sub: (a: number, b: number)   => {
        return a - b;
    },

    div : (a: number, b: number) => {
        return a / b;
    },

    mul : (a: number, b: number) => {
        return a * b;
    },

    countChar : (str: string) => {
        return str.length;
    }
}